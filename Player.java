public class Player {
	private static String naam;
	private static int lvl;
	private static int score;
	private static int attempts;
	//////////////////////////////////////////////////////////////////////////////
	public Player() {
		naam = "";
		lvl = 1;
		score = 0;
		attempts = 0;
	}
	//////////////////////////////////////////////////////////////////////////////
	public void setNaam (String nam) {
		naam = nam;
	}
	//////////////////////////////////////////////////////////////////////////////
	public void incLvl() {
		lvl++;
	}
	//////////////////////////////////////////////////////////////////////////////
	public void incScore (int s_core) {
		score = score + s_core;
	}
	//////////////////////////////////////////////////////////////////////////////
	public void resetScore () {
		score = 0;
	}
	//////////////////////////////////////////////////////////////////////////////
	public void decAttempts () {
		attempts--;
	}
	//////////////////////////////////////////////////////////////////////////////
	public void setAttempts (int att) {
		attempts =  att;
	}
	//////////////////////////////////////////////////////////////////////////////
	public String getNaam() {
		return naam;
	}
	//////////////////////////////////////////////////////////////////////////////
	public static int getLvl() {
		return lvl;
	}
	//////////////////////////////////////////////////////////////////////////////
	public int getScore() {
		return score;
	}
	//////////////////////////////////////////////////////////////////////////////
	public int getAttempts(){
		return attempts;
	}
	//////////////////////////////////////////////////////////////////////////////
}
