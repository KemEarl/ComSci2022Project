public class Player {
	private static String naam;
	private static int lvl;
	private static int tot_score;
	private static int score;
	private static int attempts;
	//////////////////////////////////////////////////////////////////////////////
	public Player() {
		naam = "";
		lvl = 0;
		score = 0;
		attempts = 0;
	}
	//////////////////////////////////////////////////////////////////////////////
	public void setNaam (String naam) {
		this.naam = naam;
	}
	//////////////////////////////////////////////////////////////////////////////
	public void incLvl() {
		this.lvl++;
	}
	//////////////////////////////////////////////////////////////////////////////
	public void incScore (int score) {
		this.score = this.score + score;
	}
	//////////////////////////////////////////////////////////////////////////////
	public void incTotScore (int score) {
		this.tot_score = this.tot_score + score;
	}
	//////////////////////////////////////////////////////////////////////////////
	public void decAttempts () {
		this.attempts--;
	}
	//////////////////////////////////////////////////////////////////////////////
	public void setAttempts (int att) {
		this.attempts = this.attempts + att;
	}
	//////////////////////////////////////////////////////////////////////////////
	public String getNaam() {
		return this.naam;
	}
	//////////////////////////////////////////////////////////////////////////////
	public int getLvl() {
		return this.lvl;
	}
	//////////////////////////////////////////////////////////////////////////////
	public int getScore() {
		return this.score;
	}
	//////////////////////////////////////////////////////////////////////////////
	public int getAttempts(){
		return this.attempts;
	}
	//////////////////////////////////////////////////////////////////////////////
}